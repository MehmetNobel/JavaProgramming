package day_03_EscapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {


        System.out.println("JAVA\nPYHTON\nC");  // \n  yeni line a geçirir.

        System.out.println("\tjava is a cool language");  // bir tablık boşluk bırakır.

        System.out.println("/  /   /  \\   \\   \\\\");  // bir back slash yazmak için 2 backslash kullanmalıyız.

        System.out.println("zeynep şöyle dedi \" nasılsın \" "); //    \"    tırnak içine almak için kullanılır..


    }
}
