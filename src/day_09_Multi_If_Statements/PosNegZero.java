package day_09_Multi_If_Statements;

public class PosNegZero {

    public static void main(String[] args) {

        int x=-5;

        if (x>0) {

            System.out.println("x pozitiftir " + x);
        }
        else if (x<0) {

            System.out.println("x negatiftir " + x);
        }
        else {

            System.out.println("x sıfırdır " + x);
        }



    }
}
